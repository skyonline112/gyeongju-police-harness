def search_laws(query: str) -> str:
    """
    민원 본문/제목(query)을 기반으로 키워드 1차 검색을 수행하고
    결과를 마크다운 텍스트로 합쳐서 반환합니다.
    """
    matched = []
    # 쿼리에서 공백 제거하고 소문자 변환
    query_clean = query.replace(" ", "").lower()
    
    for item in LAWS_DB:
        # 키워드 매칭 검증
        for kw in item["keywords"]:
            if kw.lower() in query_clean:
                matched.append(item)
                break
                
    if not matched:
        return "로컬 데이터베이스에 매칭되는 구체적인 규정이 없습니다. LLM 자체 지식을 사용해 근거를 검색해 주십시오."
        
    result = "### 로컬 DB 검색된 관련 법령 및 규정\n"
    for i, m in enumerate(matched, 1):
        result += f"\n**[{i}] {m['law_title']} ({m['category']})**\n{m['content']}\n"
    return result
